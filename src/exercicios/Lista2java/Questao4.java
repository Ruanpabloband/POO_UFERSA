//4. Escreva um programa que exiba os números de 1 a 100 na tela em ordem decrescente.

package exercicios.Lista2java;

public class Questao4 {

    public void ordemDecrescente(){

        for (int i = 100; i > 0 ; i--) {
            System.out.println(i);
        }
    }

    public void run(){
        ordemDecrescente();
    }


}
